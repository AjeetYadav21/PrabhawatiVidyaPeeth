// --------------------- LANGUAGE HANDLER -----------------------

let currentLang = localStorage.getItem("preferredLanguage") ||
    (navigator.language.startsWith("hi") ? "hi" : "en");

function translatePage(lang) {
    currentLang = lang;
    localStorage.setItem("preferredLanguage", lang);

    const dictionary = translations[lang] || translations["en"];

    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");

        if (!dictionary[key]) return;

        if (el.placeholder !== undefined) {
            el.placeholder = dictionary[key];
        } else {
            el.innerHTML = dictionary[key];
        }
    });

    // Update toggle button label
    const toggle = document.getElementById("langToggle");
    if (toggle) {
        toggle.textContent = lang === "en" ? "HI" : "EN";
        toggle.setAttribute("aria-label", lang === "en" ? "Switch to Hindi" : "Switch to English");
    }

    document.documentElement.lang = lang;
}

document.addEventListener("DOMContentLoaded", () => {
    translatePage(currentLang);

    const toggleBtn = document.getElementById("langToggle");
    if (toggleBtn) {
        toggleBtn.addEventListener("click", () => {
            translatePage(currentLang === "en" ? "hi" : "en");
        });
    }
});
